function [ tPFD2 ] = PlotPFD2( handle )
%PLOTPFD2 Summary of this function goes here
%   Detailed explanation goes here
% Plot SPD and ALT Background Boxes
hold(handle,'on');

% FD - Flight Director (center: 25,-23.5)
tPFD2.FDROLL  = plot(handle,[25 25],-[17 31],'-m','LineWidth',2);
tPFD2.FDPITCH = plot(handle,[17 33],-[23.5 23.5],'-m','LineWidth',2);


% Center White Cross Hair
plot(handle,[23.5 26.5],[-23.5 -23.5],'w-',...
    'LineWidth',2);
plot(handle,[25 25],[-23.5 -22.25],'w-',...
    'LineWidth',2);
% Center White Square
scatter(handle,25,-23.5,'Marker','square',...
     'MarkerEdgeColor',[1 1 1],...
     'MarkerFaceColor',[0 0 0],...
     'LineWidth',2,...
     'SizeData',35);
 
% 
text(handle,1,-23.5,'              ',...
    'FontSize',12,...
    'FontName','Agency FB',...
    'HorizontalAlignment','left',...
    'VerticalAlignment','middle',...
    'BackgroundColor','k',...
    'LineWidth',1.5,...
    'EdgeColor','y');
text(handle,46.75,-23.5,'         ',...
    'FontSize',12,...
    'FontName','Agency FB',...
    'HorizontalAlignment','right',...
    'VerticalAlignment','middle',...
    'BackgroundColor','k',...
    'LineWidth',1.5,...
    'EdgeColor','y');

% ARSP Center Yellow Marker
plot(handle,[9 10],-[23.5 23.5],...
    'LineWidth',2,...
    'Color','y');

% HSI Center Yellow Marker
plot(handle,[25 25],-[38 40.5],...
    'LineWidth',1.5,...
    'Color','y');

tPFD2.ARSP = text(handle,7,-23.5,'--.-',...
    'FontSize',16,...
    'FontName','Agency FB',...
    'HorizontalAlignment','right',...
    'VerticalAlignment','middle',...
    'Color','g');

tPFD2.BARO = text(handle,46.5,-23.5,'---',...
    'FontSize',16,...
    'FontName','Agency FB',...
    'HorizontalAlignment','right',...
    'VerticalAlignment','middle',...
    'Color','g');
hold(handle,'off');
end

